CREATE TABLE IF NOT EXISTS `PROJECT` (
  `PROJECTID` int(100) NOT NULL AUTO_INCREMENT,
  `PROJECTNAME` varchar(30) NOT NULL,
  `DESCRIPTION` varchar(100)NOT NULL,
  `PROVINCEID` int(100)NOT NULL,
  `ADDRESS` varchar(100)NOT NULL,
  `START_DATE` date NOT NULL,
  `FINISH_DATE` date NOT NULL,
  `CUSTOMER_NAME` varchar(20) NOT NULL,
  `CUSTOMER_PHONE` varchar(10) NOT NULL,
  `EMPLOYEEID` int(100) NOT NULL,
  `BUDGET` INTEGER(20) NOT NULL,
  `CRITICAL_BUDGET` INTEGER(5)NOT NULL, 
  `TOTAL_HIRE_EMPLOYEE` INTEGER(20)NOT NULL, 
  `TOTAL_USE_MATERIAL` INTEGER(20)NOT NULL, 
  `TOTAL_EXPENSE` INTEGER(20)NOT NULL, 
  `PERCENT_STATUS` INTEGER(3) NOT NULL,
  `CREATE_USER` varchar(10) NOT NULL,
  `CREATE_DATE` timestamp(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  `UPDATE_USER` varchar(10) NOT NULL,
  `UPDATE_DATE` timestamp(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),  
  PRIMARY KEY (`PROJECTID`),
  foreign key (EMPLOYEEID) references EMPLOYEE(EMPLOYEEID),
  foreign key (PROVINCEID) references PROVINCE(PROVINCEID)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 AUTO_INCREMENT=3 ;

